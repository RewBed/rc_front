export function settingValue(
  settings: Setting[],
  category: string,
  key: string,
): string {
  const value = settings.find(
    (setting) => setting.category === category && setting.key === key,
  )?.value;
  return typeof value === "string" || typeof value === "number"
    ? String(value)
    : "";
}

